package session3;

public class RealtionalOperators {

    public static void main(String[] args) {
        int a = 7;
        int b = 10;

        System.out.println("A > B: " + (a > b));
        System.out.println("A < B: " + (a < b));
        System.out.println("A equals B: " + (a == b));
        System.out.println("A diff B: " + (a != b));
    }
}
